#include "3-calc.h"

/**
 * op_add - do the op_thng
 * @a: first operand
 * @b: second operand
 * Return: result of op_thng
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - do the op_thng
 * @a: first operand
 * @b: second operand
 * Return: result of op_thng
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - do the op_thng
 * @a: first operand
 * @b: second operand
 * Return: result of op_thng
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - do the op_thng
 * @a: first operand
 * @b: second operand
 * Return: result of op_thng
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - do the op_thng
 * @a: first operand
 * @b: second operand
 * Return: result of op_thng
 */
int op_mod(int a, int b)
{
	return (a % b);
}
